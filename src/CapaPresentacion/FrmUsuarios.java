    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
    if(txtidempleado.getText().length()==0){
       JOptionPane.showConfirmDialog(rootPane, "ingrese nombre del empleado");
       txtidempleado.requestFocus();
       return;
       
       }
        if(txtusuario.getText().length()==0){
       JOptionPane.showConfirmDialog(rootPane, "ingrese un usuario");
       txtusuario.requestFocus();
       return;
       
       }
         if(txtpassword.getText().length()==0){
       JOptionPane.showConfirmDialog(rootPane, "ingrese un password");
       txtpassword.requestFocus();
       return;
       
       }
         M_Usuario dts = new M_Usuario();
         N_Usuario func = new N_Usuario();
         
         dts.setIdempleado(txtidempleado.getText());
         dts.setUsuario(txtusuario.getText());
         dts.setPassword(txtpassword.getText());    
        int seleccionado = cboacceso.getSelectedIndex();
        dts.setAcceso((String) cboacceso.getItemAt(seleccionado));
         seleccionado=cboestado.getSelectedIndex();
          dts.setEstado((String) cboestado.getItemAt(seleccionado));
          
          if(accion.equals("guardar")){
          if(func.insertar(dts)){
          JOptionPane.showConfirmDialog(rootPane, "cleinte registrado");
          mostrar("");
          Limpiar();
          inhabilitar();
          }
          }else if(accion.equals("editar")){
          
          dts.setIdusuario(Integer.parseInt(txtidusuario.getText()));
          if(func.editar(dts)){
          JOptionPane.showConfirmDialog(rootPane, "cleinte editado");
          mostrar("");
          Limpiar();
          inhabilitar();
          }
          
          
          }
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
     
         M_Usuario dts = new M_Usuario();
         N_Usuario func = new N_Usuario();
         if(!txtidusuario.getText().equals("")){
         int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Desea eliminar el usuario","Confiramr",2);
         if(confirmacion==0){
         dts.setIdusuario(Integer.parseInt(txtidusuario.getText()));
         func.eliminar(dts);
         mostrar("");
         inhabilitar();
         }
         }
        
    }//GEN-LAST:event_btneliminarActionPerformed

