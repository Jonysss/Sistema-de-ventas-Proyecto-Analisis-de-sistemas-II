    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
       btnguardar.setText("Editar");
       habilitar();
       btneliminar.setEnabled(true);
       accion="editar";
       int fila= tablalistado.rowAtPoint(evt.getPoint());
       
       txtidusuario.setText(tablalistado.getValueAt(fila,0).toString());
       txtidempleado.setText(tablalistado.getValueAt(fila,1).toString());
       txtapellidos.setText(tablalistado.getValueAt(fila,2).toString());
       txtnombre.setText(tablalistado.getValueAt(fila,3).toString());
       txtusuario.setText(tablalistado.getValueAt(fila,4).toString());
       txtpassword.setText(tablalistado.getValueAt(fila,5).toString());
       cboacceso.setSelectedItem(tablalistado.getValueAt(fila,6).toString());
       cboestado.setSelectedItem(tablalistado.getValueAt(fila,7).toString());
    }//GEN-LAST:event_tablalistadoMouseClicked

